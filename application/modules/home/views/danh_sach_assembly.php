<h1><span style="font-weight: 400;">Danh sách các Assembly hay nhất khi dùng tool</span></h2>
<span style="font-weight: 400;">Đây là bài viết tổng hợp đầy đủ nhất những Ass chức năng (Utilities) tốt cho tool L# cần thiết cho các bạn, nó thực sự rất hữu ích, hãy tham khảo và cài đặt. Để cài đặt được một Assembly thì bạn mở L# lên sau đó ấn vào AssemblyDB và gõ tên Assembly cần cài. Chờ tool cài một lát, xong bạn quay lại phần Assemblies để tích chọn Assembly vừa cài.<br/></span>

<br/><b>– Ezevade</b>

<span style="font-weight: 400;">Chức năng:</span>
<ul>
	<li style="font-weight: 400;"><span style="font-weight: 400;">Cảnh báo, vẽ cách skill định hướng</span></li>
	<li style="font-weight: 400;"><span style="font-weight: 400;">Tự động né các kĩ năng định hướng</span></li>
	<li style="font-weight: 400;"><span style="font-weight: 400;">Tự động sử dụng các kĩ năng dịch chuyển, tốc biến để tránh các chiêu cuối của các tướng như Annie, Maphite, Sona, Amumu …</span></li>
</ul>
