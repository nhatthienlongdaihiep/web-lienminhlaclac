<span style="font-weight: 400;">Chức năng:</span>
<ul>
	<li style="font-weight: 400;"><span style="font-weight: 400;">Tự động bơm máu, mana khi máu hoặc mana xuống dưới mức nào đó, chúng ta có thể cài đặt được mức này theo % của máu, mana</span></li>
	<li style="font-weight: 400;"><span style="font-weight: 400;">Tự động sử dụng những trang bị có thể kích hoạt được như đồng hồ cát, tim băng, gươm của vua vô danh … những lúc thích hợp</span></li>
